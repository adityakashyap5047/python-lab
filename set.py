#Write a python program to create 2 set of integers and perform the following operations

set1 = {1, 2, 3, 4, 5}
set2 = {4, 5, 6, 7, 8}

#1. Union
print("Union of two sets: ", set1.union(set2))

#2. Intersection
print("Intersection of two sets: ", set1.intersection(set2))

#3. Difference
print("Difference of two sets: ", set1.difference(set2))