# Write a python program to perform following operations on tuple

tup_1 = (1, 2, 3, 4, 5)
tup_2 = (6, 7, 8, 9, 10)

# concatenation of tuple
tup_3 = tup_1 + tup_2
print(tup_3)

# repetition of tuple
tup_4 = tup_1 * 2
print(tup_4)

# indexing
print(tup_1[0])
print(tup_1[2])

# slicing
print(tup_2[1:3])